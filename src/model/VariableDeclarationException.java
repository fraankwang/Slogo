package model;

@SuppressWarnings("serial")
public class VariableDeclarationException extends Exception {

	public VariableDeclarationException() {

	}

	public VariableDeclarationException(String message) {
		super(message);

	}

	public VariableDeclarationException(Throwable cause) {
		super(cause);

	}

	public VariableDeclarationException(String message, Throwable cause) {
		super(message, cause);

	}

	public VariableDeclarationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
